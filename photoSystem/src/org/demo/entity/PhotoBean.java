package org.demo.entity;

public class PhotoBean {
	private int id;
	private String name;
	private int count;
	private String type;
	private String email;
	private String tel;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	@Override
	public String toString() {
		return "PhotoBean [id=" + id + ", name=" + name + ", count=" + count + ", type=" + type + ", email=" + email
				+ ", tel=" + tel + "]";
	}
}
