
public class Test1 {
	
	public static void main(String[] args) {
		
		
		 //for循环
		for(int i=1;i<=10;i++){
			
			System.out.println("第"+i+"次");
			
		}
		
		System.out.println("**********************");
		
		//while循环
		int i=1;
		while(i<=10){
			
			System.out.println("第"+i+"次");
			i++;
		}
		
		System.out.println("**********************");
		
		//do-while循环
		i=1;
		do{
			
			System.out.println("第"+i+"次");
			i++;	
			
		}while(i<=10);
		
		
		
		
		
	}

}
